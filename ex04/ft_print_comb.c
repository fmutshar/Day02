void ft_print_comb(void)
{
	int a, b, c;
    for (a = 0; a <= 9; a++) {
     for (b = a+1; b <= 9; b++) {
      for (c = b+1; c <= 9; c++) {
        printf("%d%d%d,", a, b, c);
      }
    }
  }
}

